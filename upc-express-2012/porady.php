<h2><img src="images/sipka.gif" class="sipka" alt="Šipka" />PŘEDSTAVENÍ TELEVIZNÍHO PROGRAMU UPC EXPRESS</h2>

<hr />

<p> <img src="obrazky/upcexpress.png" alt="UPC Express" align="left" /> </p>

<p class="novinka"><strong>UPC EXPRESS</strong> je informačně-zpravodajský televizní program šířený ve všech kabelových sítích společnosti UPC Česká republika s.r.o. Stanice je zařazena do nejnižší analogové i digitální programové nabídky, kterou přijímá automaticky každý abonent této kabelové televize. Celkem jej tedy mohou na svých obrazovkách sledovat takřka 2 miliony diváků v téměř 600 tisících domácnostech ve 370 lokalitách České republiky. Program UPC EXPRESS je v současnosti distribuován v devíti regionálních mutacích.</p>




